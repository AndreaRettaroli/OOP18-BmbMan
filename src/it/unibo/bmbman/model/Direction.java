package it.unibo.bmbman.model;
/**
 * Define the direction in which an entity can move.
 *
 */
public enum Direction {
    /**
     * right direction.
     */
    RIGHT,
    /**
     * left direction.
     */
    LEFT,
    /**
     * up direction.
     */
    UP,
    /**
     * down direction.
     */
    DOWN,
    /**
     * no direction, the player is idle.
     */
    IDLE;
}
